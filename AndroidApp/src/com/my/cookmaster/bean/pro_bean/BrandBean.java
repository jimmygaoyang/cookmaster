package com.my.cookmaster.bean.pro_bean;

public class BrandBean {
	private Long brand_id;
	private String name;
	private String corpor_name;
	private String tel_name;
	private String address;
	private String url;
	private Long ref;
	public Long getBrand_id() {
		return brand_id;
	}
	public void setBrand_id(Long brand_id) {
		this.brand_id = brand_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCorpor_name() {
		return corpor_name;
	}
	public void setCorpor_name(String corpor_name) {
		this.corpor_name = corpor_name;
	}
	public String getTel_name() {
		return tel_name;
	}
	public void setTel_name(String tel_name) {
		this.tel_name = tel_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Long getRef() {
		return ref;
	}
	public void setRef(Long ref) {
		this.ref = ref;
	}
}
